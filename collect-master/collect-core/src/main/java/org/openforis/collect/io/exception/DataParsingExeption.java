public class DataParsingExeption extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public DataParsingExeption() {
		super();
	}

	public DataParsingExeption(String message, Throwable cause) {
		super(message, cause);
	}

	public DataParsingExeption(String message) {
		super(message);
	}

	public DataParsingExeption(Throwable cause) {
		super(cause);
	}

	
}
